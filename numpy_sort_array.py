import numpy as np
arr=np.array([22,63,11,55,33,14,32,77,91,17])
# Note: This method returns a copy of the array, leaving the original array unchanged.
print(np.sort(arr))  

arr=np.array([ "banana","apple", "cherry", "watermelon", "grapes","pineapple","mango"])
print(np.sort(arr))

# Sorting a boolean algebra
arr=np.array([True,False,True])
print(arr)
# Sorting a 2-d array
arr=np.array([[12,34,75,43],[56,23,98,31]])
print(np.sort(arr))


            




